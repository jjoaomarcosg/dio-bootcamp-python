# INTEIRO PARA FLOAT

preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

# FLOAT PARA INTEIRO 

precoo = 10.30
print(precoo)

precoo = int(precoo)
print(precoo)

# CONVERSÃO POR DIVISAO 

preço = 10
print(preço)
 
print(preço / 2)
print(preço // 2)

# NUMERICO PRA STRING
preçoo = 10.50
idade = 28

print(str(preçoo))
print(str(idade))

texto = f'idade {idade} preço {preçoo}'
print(texto)

# STRING PARA NÚMERO

preç = '10.50'
