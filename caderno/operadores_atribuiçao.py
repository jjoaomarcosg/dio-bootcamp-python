# OPERADORES DE ATRIBUIÇÃO

'''
são utilizados para definir o valor inicial
ou sobrescrever o valor de uma variável
'''

#Atribuição simples
saldo = 500
print(saldo)

# Atribuição com adição
saldo = 500
saldo += 200 # adiciona

print(saldo)

#Atribuição com subtração
saldo = 500
saldo -= 100

print(saldo)

# Atribuição com multiplicação 
saldo = 500
saldo *= 2

print(saldo)

#Atribuição com divisão 
saldo = 500
saldo /= 5 

print(saldo)

saldo = 500
saldo //= 5

print(saldo)

# Atribuição com múdulo
saldo = 500
saldo %= 480

print(saldo)

# Atribuição com potencia
saldo = 500
saldo **= 2

print(saldo)

