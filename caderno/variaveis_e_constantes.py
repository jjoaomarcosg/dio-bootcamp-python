'''
VARIAVEIS E CONSTANTES 

VARIÁVEIS
- São espaços na memória do computador que armazenam valores.

CONSTANTES
- São ão espaços na memória do computador que armazenam valores imutáveis.
- Python não possui constantes.
- Porém em python podemos criar uma variável e escrever o nome dela em maiúsculo para indicar que ela é uma constante.
'''

'''
BOAS PRÁTICAS
- O padrão de nomes deve ser snake_case (letras minúsculas separadas por underline).
- Escolher nomes sugestivos
- Nome de constantes devem ser escritos em letras maiúsculas.
'''