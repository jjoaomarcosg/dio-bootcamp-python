# São operadores utilizados para comparar dois valores


# == -> Igualdade
saldo = 450
saque = 200

print(saldo == saque)

# != -> Diferença 
saldo = 450
saque = 200

print(saldo != saque)

#Maior que / Maior ou igual 
saldo = 450
saque = 200

print(saldo > saque)
print(saldo >= saque)

#Menor que / Menor ou igual 
saldo = 450
saque = 200

print(saldo < saque)
print(saldo <= saque)


