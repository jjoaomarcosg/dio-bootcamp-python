'''
FUNÇÕES DIR E HELP

DIR
- A função dir() retorna todos os atributos e funções/métodos disponíveis
para determinado tipo de dado ou variável.
ex: dir() / dir(100)


HELP
- A função help() exibe a documentação/como utilizar os atributos e funções/métodos
disponíveis para determinado tipo de dado ou variável.
ex: help() / help(str) / help(str.lower)
'''