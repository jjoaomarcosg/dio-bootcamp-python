# git add .
# git commit -m "Concluída aula X - [tema da aula]"

# 3️⃣ Enviar para o GitHub
# git push -u origin aulaX
# - Cria a branch correspondente no GitHub
# - O -u vincula a branch local à remota, facilitando futuros pushes

# 4️⃣ Voltar para a branch principal
# - Ao terminar a aula e quiser voltar para a main:
# git checkout main
# - Depois, criar a próxima branch da aula seguinte:
# git checkout -b aulaY

# 5️⃣ Dicas importantes
# - Use uma branch por aula
# - Nomeie as branches de forma clara (aula1, aula2, aula3…)
# - Sempre commit + push ao terminar a aula
# - Arquivos em diferentes pastas dentro da mesma branch da aula são normais

